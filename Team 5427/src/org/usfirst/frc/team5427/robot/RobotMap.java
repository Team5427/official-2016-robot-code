package org.usfirst.frc.team5427.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {

	public static final int MOTOR_PWMPort_FrontLeft = 0, MOTOR_PWMPort_RearLeft = 1, MOTOR_PWMPort_FrontRight = 2,
			MOTOR_PWMPort_RearRight = 3, MOTOR_PWMPort_Flywheel = 4, MOTOR_PWMPort_LeftArm = 5,
			MOTOR_PWMPort_RightArm = 6, MOTOR_PWMPort_WinchOne = 7;

}
